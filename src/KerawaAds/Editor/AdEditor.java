    private void emailsellerFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_emailsellerFocusLost
        // TODO add your handling code here:
        String EMAIL_REGEX = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
        ImageIcon icon = new ImageIcon("/home/mabian/images/valid.png");
        String emailentered = emailseller.getText().trim();
        boolean validate = emailentered.matches(EMAIL_REGEX);
        if (validate) {
            emailerror.setVisible(true);
            emailerror.setForeground(Color.GREEN);
            emailerror.setText("");
            Image resImage = icon.getImage().getScaledInstance(40, 40, Image.SCALE_DEFAULT);
            emailerror.setIcon(new ImageIcon(resImage));
            Publish.setVisible(true);
        } else {
            emailerror.setVisible(true);
            emailerror.setForeground(Color.red);
            emailerror.setIcon(new ImageIcon());
            emailerror.setText("email invalide");
            Publish.setVisible(false);
        }
    }//GEN-LAST:event_emailsellerFocusLost

