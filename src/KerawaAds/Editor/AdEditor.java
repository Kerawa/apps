    public boolean chechExtension(String xml) {
        final String XMLformat = ".xml";
        boolean xmlVal = false;
        int len = xml.length();
        String substr = xml.substring(len - 4);

        if (substr.equals(XMLformat)) {
            xmlVal = true;
            return xmlVal;
        } else {
            return xmlVal;
        }
    }//end of checkfileextension

